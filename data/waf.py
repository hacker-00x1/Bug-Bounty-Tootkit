# ── Public API ────────────────────────────────────────────────────────────────

def fingerprint(
    base_url:   str,
    timeout:    int  = 8,
    user_agent: str  = "BugBountyTool/1.0",
    probe_attacks: bool = True,
) -> dict:
    """
    Fingerprint the WAF/CDN in front of base_url.

    Returns:
      detected          – list of {name, confidence, signals_matched}
      primary_waf       – name of the top match (or None)
      rate_limit        – rate-limit probe results
      recommended_threads  – int
      recommended_delay_ms – int
      notes             – str
      all_responses     – raw probe data (for report)
    """
    # 1. Collect probe responses
    probe_urls = [urllib.parse.urljoin(base_url, CLEAN_PROBE)]
    if probe_attacks:
        for path in ATTACK_PROBES:
            probe_urls.append(urllib.parse.urljoin(base_url, path))

    responses: list[dict] = []
    with ThreadPoolExecutor(max_workers=6) as executor:
        futures = [executor.submit(_request, u, timeout, user_agent) for u in probe_urls]
        for future in as_completed(futures):
            r = future.result()
            if r:
                responses.append(r)

    if not responses:
        return {
            "detected":             [],
            "primary_waf":          None,
            "rate_limit":           {},
            "recommended_threads":  DEFAULT_TUNING[0],
            "recommended_delay_ms": DEFAULT_TUNING[1],
            "notes":                "Could not reach target for WAF probing.",
            "probes_sent":          len(probe_urls),
        }

    # 2. Score each WAF signature
    scored: list[dict] = []
    for name, matchers in WAF_SIGNATURES:
        score = _score_waf(name, matchers, responses)
        if score > 0:
            total_signals = len(matchers)
            confidence    = min(100, round(score / max(total_signals, 1) * 100))
            scored.append({
                "name":            name,
                "score":           score,
                "signals_matched": score,
                "total_signals":   total_signals,
                "confidence":      confidence,
            })

    scored.sort(key=lambda x: x["score"], reverse=True)

    # 3. Collect interesting headers for the report
    header_set: dict[str, str] = {}
    for resp in responses:
        for k, v in resp["headers"].items():
            interesting = any(tok in k for tok in [
                "server", "x-", "cf-", "via", "set-cookie", "powered-by",
                "cache", "akamai", "fastly", "incap", "datadome",
            ])
            if interesting and k not in header_set:
                header_set[k] = v

    # 4. Rate-limit probe
    rl = _probe_rate_limit(base_url, timeout, user_agent)

    # 5. Recommended tuning
    primary = scored[0]["name"] if scored else None
    if primary and primary in WAF_TUNING:
        rec_threads, rec_delay, notes = WAF_TUNING[primary]
    else:
        rec_threads, rec_delay, notes = DEFAULT_TUNING

    if rl["triggered"] and rec_delay < 300:
        rec_delay = max(rec_delay, 400)
        notes += " Rate-limiting confirmed — crawl delay bumped."
    if rl["pct_blocked"] > 50:
        rec_threads = min(rec_threads, 5)
        notes += " High block rate — threads capped at 5."

    return {
        "detected":             scored,
        "primary_waf":          primary,
        "interesting_headers":  header_set,
        "rate_limit":           rl,
        "recommended_threads":  rec_threads,
        "recommended_delay_ms": rec_delay,
        "notes":                notes,
        "probes_sent":          len(probe_urls),
    }


