# ── Probe payloads ────────────────────────────────────────────────────────────

CLEAN_PROBE   = "/"

ATTACK_PROBES = [
    "/?id=1'%20OR%20'1'='1",                      # SQLi
    "/?q=<script>alert(1)</script>",               # XSS
    "/?file=../../../../etc/passwd",               # LFI
    "/?url=https://evil.com",                      # Open redirect
    "/?search=UNION+SELECT+NULL,NULL,NULL--",      # SQLi union
]


# ── HTTP helpers ──────────────────────────────────────────────────────────────

def _request(url: str, timeout: int, user_agent: str) -> Optional[dict]:
    try:
        req = urllib.request.Request(
            url,
            headers={"User-Agent": user_agent, "Accept": "*/*"},
        )
        with urllib.request.urlopen(req, timeout=timeout) as resp:
            headers = {k.lower(): v.lower() for k, v in resp.headers.items()}
            body    = resp.read(8192).decode("utf-8", errors="replace").lower()
            return {"status": str(resp.status), "headers": headers, "body": body, "url": url}
    except urllib.error.HTTPError as e:
        headers = {k.lower(): v.lower() for k, v in e.headers.items()}
        try:
            body = e.read(8192).decode("utf-8", errors="replace").lower()
        except Exception:
            body = ""
        return {"status": str(e.code), "headers": headers, "body": body, "url": url}
    except Exception:
        return None


def _match(response: dict, field: str, pattern: str) -> bool:
    if field.startswith("header:"):
        hdr_name = field[7:]
        hdr_val  = response["headers"].get(hdr_name, "")
        return pattern == "" or pattern in hdr_val
    elif field == "body":
        return pattern in response["body"]
    elif field == "status":
        return response["status"] == pattern
    elif field == "server":
        return pattern in response["headers"].get("server", "")
    return False


def _score_waf(name: str, matchers: list, responses: list[dict]) -> int:
    score = 0
    for resp in responses:
        for field, pattern in matchers:
            if _match(resp, field, pattern):
                score += 1
    return score


# ── Rate-limit probe ──────────────────────────────────────────────────────────

def _probe_rate_limit(base_url: str, timeout: int, user_agent: str, burst: int = 30) -> dict:
    """
    Fire `burst` rapid requests and count 429/503 responses.
    Returns: triggered (bool), first_block_at (int|None), pct_blocked (float)
    """
    statuses = []
    block_at = None

    def _fetch(i: int):
        resp = _request(base_url, timeout, user_agent)
        return (i, resp["status"] if resp else "err")

    with ThreadPoolExecutor(max_workers=burst) as executor:
        futures = {executor.submit(_fetch, i): i for i in range(burst)}
        for future in as_completed(futures):
            idx, status = future.result()
            statuses.append((idx, status))

    blocked = [(i, s) for i, s in statuses if s in ("429", "503")]
    triggered = len(blocked) > 0
    if blocked:
        block_at = min(i for i, _ in blocked)

    pct_blocked = len(blocked) / burst * 100 if burst else 0.0
    return {
        "triggered":       triggered,
        "burst_size":      burst,
        "blocked_count":   len(blocked),
        "first_block_at":  block_at,
        "pct_blocked":     round(pct_blocked, 1),
    }


