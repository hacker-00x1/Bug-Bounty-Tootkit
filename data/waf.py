"""
WAF / rate-limit fingerprinter.

Sends a series of probe requests (clean, suspicious, malicious) and
analyses response headers, status codes, and body content to identify
the protection layer in front of the target.

Also probes for rate-limiting by firing a burst of rapid requests and
watching for 429 / 503 responses.

Returns a structured result the scanner uses to auto-tune thread count
and crawl delay for the rest of the scan.
"""

